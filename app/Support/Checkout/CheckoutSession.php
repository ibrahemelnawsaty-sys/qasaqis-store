<?php

declare(strict_types=1);

namespace App\Support\Checkout;

use Illuminate\Contracts\Session\Session;
use Illuminate\Support\Str;

/**
 * حالة محاولة الدفع المحفوظة في الجلسة (M7 — رحلة العميل، المرحلة 5).
 *
 * مفتاح منع التكرار يعيش هنا وليس في حقل مخفي في النموذج، وهذا قرار أمني مقصود
 * (بند 4.1): لو أرسله العميل لأمكن لأي شخص أن يبعث مفتاح طلب غيره فيستقبل نتيجة
 * ذلك الطلب — أي رابطه الموقّع وتفاصيله. اشتقاقه من الجلسة يغلق هذا الباب تمامًا،
 * ويجعله غير قابل للتزوير أصلًا لأنه لا يمرّ عبر المتصفح.
 *
 * دورة الحياة: مفتاح جديد عند كل عرض لصفحة الدفع، ولا يُنسى بعد النجاح.
 * هذا الاختيار مقصود ويغطي ثلاث حالات معًا:
 *  - النقر المزدوج: الطلبان يحملان مفتاح العرض نفسه ⇒ الثاني يُردّ بنتيجة الأول.
 *  - إعادة إرسال النموذج (F5 بعد اكتمال الطلب): لا عرض جديد ⇒ المفتاح لم يتغيّر
 *    ⇒ يُردّ بنتيجة الطلب الأول بدل إنشاء طلب ثانٍ. ولو نُسي المفتاح عند النجاح
 *    لانفتحت هذه الثغرة بالضبط.
 *  - شراء ثانٍ مشروع لاحقًا: تمرّ العميلة بصفحة الدفع من جديد ⇒ مفتاح جديد
 *    ⇒ طلب جديد كما ينبغي.
 */
final class CheckoutSession
{
    private const IDEMPOTENCY_KEY = 'checkout.idempotency_key';

    /**
     * يبدأ محاولة دفع جديدة بمفتاح جديد. يُستدعى عند عرض صفحة الدفع فقط — كل عرض
     * محاولة مستقلة، وكل إرسال من العرض نفسه هو المحاولة ذاتها.
     */
    public static function beginAttempt(Session $session): string
    {
        $key = (string) Str::uuid();
        $session->put(self::IDEMPOTENCY_KEY, $key);

        return $key;
    }

    /**
     * null = لا مفتاح في الجلسة (إرسال لم يمرّ بصفحة الدفع، أو جلسة انتهت). لا
     * يُسقِط الطلب، بل يعني أنه بلا حماية من التكرار — سلوك ما قبل هذه الميزة.
     */
    public static function currentIdempotencyKey(Session $session): ?string
    {
        $key = $session->get(self::IDEMPOTENCY_KEY);

        return is_string($key) && $key !== '' ? $key : null;
    }
}
